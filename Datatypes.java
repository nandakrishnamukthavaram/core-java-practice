public class Datatypes {
    public static void main(String[] args) {
        // Integers
        long numbe = Long.MAX_VALUE;
        int number = Integer.MAX_VALUE;
        short numb = Short.MAX_VALUE;
        byte numb1 = Byte.MAX_VALUE;
        // Decimals
        double decima = Double.MAX_VALUE + Double.MAX_EXPONENT;
        float decimal = Float.MAX_VALUE + Float.MAX_EXPONENT;
        // Character
        char character = 'K';
        // Boolean
        boolean bool = true;

        // Print
        System.out.println("Long: " + numbe);
        System.out.println("Int: " + number);
        System.out.println("Short: " + numb);
        System.out.println("Byte: " + numb1);
        System.out.println("Double: " + decima);
        System.out.println("Float: " + decimal);
        System.out.println("Char: " + character);
        System.out.println("Boolean: " + bool);
    }
}
